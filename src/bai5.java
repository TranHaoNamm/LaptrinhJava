public class bai5 {//các phép toán cơ bản

    public static void main(String[] args) {
        int a = 11;
        int b = 5;

        //Phép cộng
        System.out.println("a + b = " +(a+b));
        //Phép trừ
        System.out.println("a - b = " +(a-b));
        //phép nhân
        System.out.println("a * b = " +(a * b));
        //phép chia
        System.out.println("a/b = " +((double)a/b));
        //phép chia lấy dư
        System.out.println("a%b = " +(a%b));
    }
}

public class bai6 {//Toán tử gán

    public static void main(String[] args) {
        int x = 8;
        //gán cộng
        x += 5; // x = x + 5
        System.out.println("x = " +x);
        //gán trừ
        int x2 = 8;
        x2 -= 5; //x2 = x2 - 5
        System.out.println("x2 = " +x2);
        //gán nhân
        int x3 = 8;
        x3 *= 5; //x3 = x3 * 5
        System.out.println("x3 = " +x3);
        //Toán tử so sánh
        int a = 7;
        int b = 9;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);


    }
}

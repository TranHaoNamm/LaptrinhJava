public class bai4 {//ép kiểu dữ liệu

    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        double kq = (double) a/b;
        //ép kiểu rộng
        System.out.println("kết quả a/b là: " +kq);
        //ép kiểu hẹp
        int c = 128;
        byte d = (byte) c;
        System.out.println("c = " +c);
        System.out.println("d = " +d);

        int e = 15;
        byte f = (byte) e;
        System.out.println("e = " +e);
        System.out.println("f = " +f);


    }
}
